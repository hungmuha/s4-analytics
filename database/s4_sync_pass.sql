        DELETE FROM violation;
    ELSE
        DELETE FROM violation vi
        WHERE EXISTS (
            SELECT 1
            FROM v_flat_violation@s4_warehouse vv
            WHERE vv.last_updt_dt >= v_start_dt
            AND vv.hsmv_rpt_nbr = vi.hsmv_rpt_nbr
        );
    END IF;

    INSERT INTO violation (
        hsmv_rpt_nbr,
        hsmv_rpt_nbr_trunc,
        person_nbr,
        citation_nbr,
        citation_nbr_trunc,
        key_crash_dt,
