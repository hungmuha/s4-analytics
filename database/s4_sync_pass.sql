        rptg_agncy_type,
        key_rptg_unit,
        rptg_unit_nm,
        rptg_unit_short_nm,
        key_ar_of_init_impact,
        ar_of_init_impact,
        key_body_type,
        body_type,
        key_cargo_body_type,
        cargo_body_type,
        key_cmv_config,
        cmv_config,
        key_comm_non_comm,
